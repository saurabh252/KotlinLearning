fun main() {
    // var value can be reassigned
    var score:Int = 10;
    println("Score :$score")
    var temperature:Float = 24.5F
    println("Temperature : $temperature")
    var height:Double = 153.5
    println("Height :$height")
    var message:String = "Hello Kotlin"
    println("Message : $message")
    var raining:Boolean = false
    println("Raining :$raining")
    var word:Char = 'A'
    println("Word :$word")
    //val value can't be reassigned
    val name:String = "Hello Saurabh"
    println("Name :$name")
//    name = "Hello Kotlin"
}